#include "../push_swap.h"

void	rotate(t_list **stack)
{
	t_list	*temp;
	t_list	*last;

	temp = *stack;
	if (*stack == NULL)
		return ;
	last = ft_lstlast(*stack);
	(*stack) = (*stack)->next;
	last->next = temp;
	temp->next = NULL;
}

void	ra(t_list **stack_a)
{
	rotate(stack_a);
	ft_putendl_fd("ra", 1);
}

void	rb(t_list **stack_b)
{
	rotate(stack_b);
	ft_putendl_fd("rb", 1);
}

void	rr(t_list **stack_a, t_list **stack_b)
{
	rotate(stack_a);
	rotate(stack_b);
	ft_putendl_fd("rr", 1);
}
